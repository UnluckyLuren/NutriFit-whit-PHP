const ContainerProd = document.getElementById('ContainerProdHTML'),
productoBuscado = document.getElementById('buscadorGeneral'),
buscarBtn = document.getElementById('buscarBtn'),
containerfooter = document.getElementById('containerfooterBuscador');
containerfooter.classList.add('disNone'),
TitleChange = document.getElementById('TitleChange');

//Construccion del HTML

const crearHtml = id => {

    const html = ` <div class="fexcajasPR"><img src="${Linksimg[id]}" class="imgProductos" alt="${nameProductos[id]}"></a>
            <p class="titleProductCount">${nameProductos[id]}</p> </div> `;

    const div = document.createElement('div');
    div.innerHTML = html;
    
    ContainerProd.append( div );
}


// Filtro

const Filtrar = () => {

    ContainerProd.innerHTML="";
    const buscar = productoBuscado.value.toLowerCase();

    for ( let i=0; i<nameProductos.length; i++ ) {
        let nombre = nameProductos[i].toLowerCase();

        if (nombre.indexOf(buscar) !== -1) {
            crearHtml(i);
        } 
    }

    if (ContainerProd.innerHTML === "") {
        ContainerProd.innerHTML = ` <br> <pre class="titulo_de_servicios_productos">    No se encontraron resultados</pre>`
    }
}

productoBuscado.addEventListener('keyup', () => {
    Filtrar();
    TitleChange.textContent="Busqueda";
});

buscarBtn.addEventListener('click', () => {

    if ( TitleChange.textContent==="Busqueda" ) {
        Filtrar();
    }

});


// Creación de productos de acuerdo al filtro


const lista = document.getElementById('ListaProductos');


const ProductsFiltrados = (min, max) => {

    ContainerProd.innerHTML="";

    for ( let i=min; i<=max; i++ ) {
        crearHtml(i);
    }
}

lista.addEventListener('click', e => {

    let tag = e.target.textContent;
    
    if ( tag === "Cereales/Carbohidratos") {

       ProductsFiltrados(0, 75);
       TitleChange.textContent=tag;

    } else if ( tag === "Frutas") {

        ProductsFiltrados(76, 143);
        TitleChange.textContent=tag;

    } else if ( tag === "Grasas") {

        ProductsFiltrados(144, 192);
        TitleChange.textContent=tag;

    } else if ( tag === "Leguminosas") {

        ProductsFiltrados(193, 221);
        TitleChange.textContent=tag;

    } else if ( tag === "Proteínas") {

        ProductsFiltrados(222, 302);
        TitleChange.textContent=tag;

    } else if ( tag === "Verduras") {

        ProductsFiltrados(303, 377);
        TitleChange.textContent=tag;

    } 

});


